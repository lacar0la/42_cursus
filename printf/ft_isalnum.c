#include <stdio.h>
#include <unistd.h>

int	ft_isalnum(int c)
{
	if (c == '\0')
	{
		return (0);
	}
	else
	{
		if (c >= '0' && c <= '9')
		{
			return (1);
		}
		if ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		{
			return (1);
		}
	}
	return (0);
}
